/**
 * RFC 5322 準拠の簡易メールアドレスバリデーション正規表現。
 *
 * 完全なRFC準拠は複雑すぎるため、一般的なメールアドレスをカバーする正規表現を使用する。
 * サーバー側でも別途バリデーションを行うため、フロントエンドでは簡易チェックに留める。
 */
export const EMAIL_REGEX = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
